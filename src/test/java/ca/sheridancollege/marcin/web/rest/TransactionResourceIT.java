    @Test
    @Transactional
    void putNewTransaction() throws Exception {
        // Initialize the database
        transactionRepository.saveAndFlush(transaction);

        int databaseSizeBeforeUpdate = transactionRepository.findAll().size();

        // Update the transaction
        Transaction updatedTransaction = transactionRepository.findById(transaction.getId()).get();
        // Disconnect from session so that the updates on updatedTransaction are not directly saved in db
        em.detach(updatedTransaction);
        updatedTransaction.subTotal(UPDATED_SUB_TOTAL).tax(UPDATED_TAX).total(UPDATED_TOTAL).lastFourDigits(UPDATED_LAST_FOUR_DIGITS);

        restTransactionMockMvc
            .perform(
                put(ENTITY_API_URL_ID, updatedTransaction.getId())
                    .contentType(MediaType.APPLICATION_JSON)
                    .content(TestUtil.convertObjectToJsonBytes(updatedTransaction))
            )
            .andExpect(status().isOk());

        // Validate the Transaction in the database
        List<Transaction> transactionList = transactionRepository.findAll();
        assertThat(transactionList).hasSize(databaseSizeBeforeUpdate);
        Transaction testTransaction = transactionList.get(transactionList.size() - 1);
        assertThat(testTransaction.getSubTotal()).isEqualTo(UPDATED_SUB_TOTAL);
        assertThat(testTransaction.getTax()).isEqualTo(UPDATED_TAX);
        assertThat(testTransaction.getTotal()).isEqualTo(UPDATED_TOTAL);
        assertThat(testTransaction.getLastFourDigits()).isEqualTo(UPDATED_LAST_FOUR_DIGITS);
    }

